// For Loop

{
    for (let i = 1; i <= 10; i++) {
        console.log(i);
    }
}

// While Loop

{
    let i = 20;
    while (i > 0) {
        console.log(i);
        i--;
    }
}

// Do While Loop

{
    let i = 5;
    do {
        console.log(`do while i ${i}`);
        i++;
    } while (i < 8);
}